user1=int(input("enter the value"))
user2=int(input("enter the value"))
user3=input("enter the symbol")
if user3=="+":
    print(user1+user2)
elif user3=="-":
    print(user1-user2)
elif user3=="*":
    print(user1*user2)
elif user3=="/":
    print(user1/user2)
elif user3=="%":
    print(user1%user2)
else:
    print("end")
    
